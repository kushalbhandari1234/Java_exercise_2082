

public class Grade {
    
    public static void main (String []args)
    {
        int marks = 85;
        if (marks >=90){

            System.out.println("A grade");
        }

        else if (marks >=80){
            System.out.println("B grade");
        }

        else if (marks >=70){
            System.out.println("C grade");
        }

        else{
            System.out.println("fail");
        }
    }
}
